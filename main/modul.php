//End Include Common Files

//Master Page implementation @1-357D2559
include_once(RelativePath . "/main/../Designs/d01/MasterPage.php");
//End Master Page implementation

//Initialize Page @1-5AF822AA
// Variables
$FileName = "";
$Redirect = "";
$Tpl = "";
$TemplateFileName = "";
$BlockToParse = "";
$ComponentName = "";
$Attributes = "";
$PathToCurrentMasterPage = "";
$TemplatePathValue = "";

// Events;
$CCSEvents = "";
$CCSEventResult = "";
$MasterPage = null;
$TemplateSource = "";

$FileName = FileName;
